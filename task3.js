//Create a calculator with four basic operations.

function add(a,b){
    let sum = a+b
    return sum;
}

function subtract(a,b){
    let diff = a+b
    return diff;
}

function multiply(a,b){
    let mul = a*b
    return mul;
}

function divide(a,b){
    let div = a/b
    return div;
}


console.log(add(3,4))
console.log(divide(36,4))

